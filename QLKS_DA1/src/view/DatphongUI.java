    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        cbocccd.removeAllItems();
        LKH = khsv.getAlldataKH();
        for (int i = 0; i < LKH.size(); i++) {
            cbocccd.addItem(LKH.get(i).getMaKH());
        }
        lblphong.setForeground(Color.decode("#000000"));
        lblphong.setText(String.valueOf(MaPhong));
        if (rdogio.isSelected()) {
            txtgiatien.setText(GiaTheoGio + "");
        } else {
            txtgiatien.setText(GiaTheoNgay + "");
        }
    }//GEN-LAST:event_formWindowOpened

