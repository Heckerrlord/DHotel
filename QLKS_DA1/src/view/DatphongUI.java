    public dthuephong getdataphieu() {
        SimpleDateFormat s = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            dthuephong data = new dthuephong();
            data.setMaKH(cbocccd.getSelectedItem().toString());
            data.setMaPhong(lblphong.getText());
            if (rdogio.isSelected()) {
                data.setLoaiHinhThue(1);
            } else {
                data.setLoaiHinhThue(0);
            }
            data.setNgayThue(s.format(txtngaythue.getDate()));
            data.setNgayRaDuKien(s.format(txtngaytra.getDate()));
            data.setSoNguoi(Integer.parseInt(txtnguoi.getText()));
            data.setTenUser("duysp");
            data.setTrangthai(0);
            data.setTienCoc(Double.parseDouble(txttiencoc.getText()));
            return data;
    }

