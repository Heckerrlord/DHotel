public void loadTB() {
        ArrayList<DichVu> List = dichVu_Service.getAllList();
        dtm = (DefaultTableModel) tbl_dichvu.getModel();
        dtm.setRowCount(0);
        for (DichVu x : List) {
            Object[] data = {
                x.getMadv(),
                x.getTendvl(),
                x.getDonvi(),
                x.getDongia(),
                x.getGhiChu()
            };
            dtm.addRow(data);
        }
    }

    public DichVu getForm() {
        clear();
        int check = 0;
        String madv = txtma.getText();
        String ten = txtTen.getText();
        String donvi = txtDonVi.getText();
        String dongia = txtDonGia.getText();
        String ghichu = txtGhiChu.getText();
        if (madv.trim().length() == 0) {
            tbma.setText("Chua nhap ma");
            check++;
        }
        if (ten.trim().length() == 0) {
            tbten.setText("Chua nhap Ten");
            check++;
        }
        if (donvi.trim().length() == 0) {
            tbdonnvi.setText("Chua nhap don vi");
            check++;
        }
        if (dongia.trim().length() == 0) {
            tbdongia.setText("Chua nhap Don gia");
            check++;
        }
        if (ghichu.trim().length() == 0) {
            tbghichu.setText("Chua nhap ghi chu");
            check++;
        }
        String a;
        
            a = donvi;
        
        
        double b = 0;
        try {
            b = Double.parseDouble(dongia);
        } catch (NumberFormatException e) {
            e.printStackTrace();
            check++;
            tbdongia.setText("Don gia phai la so");
        }
        if (check != 0) {
            return null;
        }
        DichVu dv = new DichVu(madv, ten, a, b, ghichu);
        return dv;
    }

