    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String madv = txtma.getText();
        DichVu dv = getForm();
        if (dv == null) {
            return;
        }
        int a = JOptionPane.showConfirmDialog(this, "Them", "Ban muon them khong", JOptionPane.YES_NO_OPTION);
        for (DichVu x : List) {
            if (x.getMadv().equalsIgnoreCase(madv)) {
                JOptionPane.showMessageDialog(this, "trung ma them that bai");
                return;
            }
        }
        if (a == 0) {
            dichVu_Service.them(dv);
            JOptionPane.showMessageDialog(this, "them thanh cong");
            loadTB();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tbl_dichvu.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "ban chua chon dong");
            return;
        }
        DichVu dv = getForm();
        if (dv == null) {
            return;
        }
        int a = JOptionPane.showConfirmDialog(this, "Ban muon sua khong", "Sua", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            dichVu_Service.sua(dv);
            JOptionPane.showMessageDialog(this, "Sua thanh cong");
            loadTB();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tbl_dichvu.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "ban chua chon dong");
            return;
        }
        String ma = tbl_dichvu.getValueAt(row, 0).toString();
        int a = JOptionPane.showConfirmDialog(this, "Ban muon Xoa khong", "Xoa", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            dichVu_Service.xoa(ma);
            JOptionPane.showMessageDialog(this, "Xoa thanh cong");
            loadTB();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        String timten = "%" + txttim.getText() + "%";
        ArrayList<DichVu> List1 = dichVu_Service.timKiem(timten);
        if (txttim.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Chua nhap noi dung tim kiem");
            loadTB();
            return;
        }
        dtm = (DefaultTableModel) tbl_dichvu.getModel();
        dtm.setRowCount(0);
        for (DichVu x : List1) {
            Object[] data = {
                x.getMadv(),
                x.getTendvl(),
                x.getDonvi(),
                x.getDongia(),
                x.getGhiChu()
            };
            dtm.addRow(data);
        }
        if (List1.size() == 0) {
            JOptionPane.showMessageDialog(this, "Khong tim thay");
        } else {
            JOptionPane.showMessageDialog(this, "tim thanh cnong");
        }
    }//GEN-LAST:event_btnTimActionPerformed

